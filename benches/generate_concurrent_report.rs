fn format_output(stdout: &str, stderr: &str) -> String {
    let mut output = String::new();
    
    if !stdout.is_empty() {
        output.push_str("STDOUT:\n");
        output.push_str(stdout);
    }
    
    if !stderr.is_empty() {
        output.push_str("\nSTDERR:\n");
        output.push_str(stderr);
    }
    
    output
}

